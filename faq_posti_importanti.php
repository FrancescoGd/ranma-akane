            <h4>St. Hebereke Jogakuin</h4>
            <p>&nbsp;</p>
            <blockquote>
              <p align="left" class="faq_reply"><strong>Scuola
              Superiore Femminile San Bacco</strong></p>
              <p>&nbsp;</p>
              <p><span class="faq_reply">È la scuola femminile
              frequentata da Kodachi ed è la scuola rivale della
              scuola Furinkan.</span></p>
