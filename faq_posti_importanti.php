            <h4>Tendo Dojo</h4>
            <p>&nbsp;</p>
            <p class="faq_reply">Un <em>dojo</em> (Scuola di Arti
            Marziali) situato a <strong>Nerima</strong>, Tokyo,
            dove la famiglia Tendo vive insieme alla famiglia
            Saotome che è loro ospite. Soun e Genma hanno stabilito
            che Ranma dovrà sposare Akane e portare avanti con lei
            le tradizioni delle due famiglie e del dojo.</p>
            <p>&nbsp;</p>
            <p>&nbsp;</p>
            <h4>Furinkan Koko</h4>
            <p align="center">&nbsp;</p>
            <p align="left" class="faq_reply"><strong>Scuola
            Superiore Furinkan</strong></p>
            <p align="center">&nbsp;</p>
            <p class="faq_reply">La scuola superiore frequentata da
            Ranma, Akane, Nabiki, Kuno e, più tardi, anche da
            Ukyo.</p>
            <p>&nbsp;</p>
            <p><span class="faq_reply"><strong class=
            "highlight_section">Nome:</strong> Più o meno significa
            "<em>La stanza della campana del
            vento</em>".</span></p>
            <p>&nbsp;</p>
            <p>&nbsp;</p>
            <h4>Neko Hanten</h4>
            <p align="left">&nbsp;</p>
            <p align="left" class="faq_reply"><strong>Ristorante
            Cinese "Il Gatto"</strong></p>
            <p align="left">&nbsp;</p>
            <p align="left" class="faq_reply">È il ristorante
            cinese gestito da Obaba e Shampoo, con l'aiuto a volte
            dello sfuggente padre di Shampoo e anche di Mousse (più
            che altro, Mousse è il loro schiavo).</p>
            <p align="left">&nbsp;</p>
            <p align="left">&nbsp;</p>
            <h4>Ucchan</h4>
            <p align="left">&nbsp;</p>
            <blockquote>
              <p class="faq_reply">È il ristorante specializzato in
              <em>okonomiyaki</em> aperto e gestito da Ukyo, dopo
              che è arrivata a Tokyo per ritrovare Ranma.</p>
              <p>&nbsp;</p>
              <p class="faq_reply"><strong class=
              "highlight_section">Nome:</strong> Deriva dalla
              contrazione di <em>"Ukyo-chan"</em>.</p>
            </blockquote>
            <p align="left">&nbsp;</p>
            <p align="left">&nbsp;</p>
            <h4>Joketsuzoku</h4>
            <p>&nbsp;</p>
            <blockquote>
              <p class="faq_reply">Un villaggio che si trova vicino
              <em>Jusenkyo</em> dove vivono le
              <em>Joketsuzoku</em>, un clan cinese di "amazzoni" a
              cui appartengono anche Obaba e Shampoo.</p>
            </blockquote>
            <p align="left">&nbsp;</p>
