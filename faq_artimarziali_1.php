				<p class="faq_reply"><strong>Pugno del Gatto</strong></p>
				<p>&nbsp;</p>
				<p class="faq_reply">Un giorno Genma, per far diventare pi&ugrave;       forte e coraggioso suo figlio Ranma, lo sottopose alla "terribile prova       dei gatti", ricoprendolo di cibo per gatti e gettandolo in un pozzo pieno       di gatti famelici. Genma aveva letto di questa prova su un antico libro che       si credeva perduto... purtroppo, la sua ottusit&agrave; non gli fece leggere       la pagina successiva che diceva che questa prova era del tutto idiota ed inutile.       Tutto quello che Ranma ha guadagnato da quella storia, &egrave; una fobia       assoluta verso i gatti. Quando l'ailurofobia di Ranma raggiunge l'apice, egli       va completamente fuori di testa comincia a comportarsi e pensare come un gatto,       graffiando e miagolando con forza e ferocia.</p>
				<p class="faq_reply">&nbsp;</p>
