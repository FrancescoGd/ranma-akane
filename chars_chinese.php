                <td valign="top" class="datatable-text">
                  <p>È la bisnonna di Shampoo, ed è molto ma molto
                  vecchia, nonché potente nella conoscenza delle
                  arti marziali. Da quando è arrivata in Giappone,
                  tra le altre cose, gestisce il ristorante cinese
                  "Neko-Hanten" insieme alla sua dolce nipotina.
                  Tanto per cambiare anche Obaba ritiene Ranma
                  responsabile di diverse cose tra le quali la
                  maledizione che ha colpito Shampoo, e cerca in
                  tutti i modi di farli sposare, ricorrendo anche a
                  dei ricatti veri e propri. Riconosce in Ranma un
                  ragazzo forte e abile e non manca mai di metterlo
                  alla prova nelle arti marziali.</p>
                  <p>Nonstante sia una vecchiaccia "mummiosa", non
                  le gradisce molto le battute sul suo aspetto,
                  credendo a volte perfino di essere una bellezza.
                  Comunque ha una grande conoscenza di tutte le
                  tecniche di arti marziali che si tramandano nel
                  suo clan e di magia.</p>
