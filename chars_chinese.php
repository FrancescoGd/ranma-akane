                            <p>Nonstante sia una     vecchiaccia "mummiosa", non le gradisce molto le battute sul suo     aspetto, credendo a volte perfino di essere una bellezza. Comunque ha una grande     conoscenza di tutte le tecniche di arti marziali che si tramandano nel suo clan     e di magia.</p>
