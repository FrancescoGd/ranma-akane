                <td valign="top" class="datatable-text">
                  <p>Probabilmente è quella che viene considerata
                  la "bambola sexy" di tutta la serie. È una
                  ragazza molto carina, disinibita, agile e a volte
                  anche violenta e crudele. Subito dopo
                  l'esperienza delle sorgenti maledette, la Guida
                  Cinese porta Ranma e Genma al Villaggio
                  Joketsuzoku dove Shampoo sta battendo tutte le
                  avversarie nella gara annuale che si tiene al suo
                  villaggio. Ranma e Genma, arrivando sfiniti ed
                  affamati (e anche trasformati!) si siedono ad un
                  banchetto preparato di tutto punto, cominciando
                  ad ingoiare senza ritegno quello che costituiva
                  il premio del torneo. Indispettita da questo,
                  Shampoo invita Ranma (ragazza) a battersi con lei
                  ma viene inesorabilmente sconfitta. Dal momento
                  che è stata battuta da una donna straniera
                  Shampoo, secondo la legge del suo villaggio, si
                  avvicina a Ranma e le dà il cosiddetto "bacio
                  della morte", il che significa che Shampoo
                  inseguirà Ranma per ogni dove, finché non l'avrà
                  uccisa.</p>
                  <p>Quando giunge a Tokyo sulle tracce di
                  Ranma-chan, Shampoo giunge al dojo dei Tendo
                  dove, per la prima volta, incontra Ranma-kun. A
                  questo punto, Shampoo cerca di attaccare Akane
                  credendo che nasconda Ranma-chan, e così
                  Ranma-kun, nel tentativo di difendere Akane,
                  batte di nuovo Shampoo la quale, invece di dargli
                  il "bacio della morte", questa volta dichiara il
                  suo amore a Ranma-kun chiamandolo "marito!".</p>
                  <p>Questa nuova svolta è dovuta sempre alla legge
                  del villaggio di origine di Shampoo. Infatti, se
                  una donna del Clan delle Amazzoni viene battuta
                  da uno straniero ci sono due possibilità: se lo
                  straniero è una donna, questa va uccisa da colei
                  che è stata battuta; se lo straniero è un uomo,
                  devono sposarsi. È per questo che Shampoo, almeno
                  all'inizio, si ritrova nella singolare situazione
                  in cui odia a morte Ranma-chan, ma vuole sposare
                  Ranma-kun!</p>
                  <p>Secondo la sua natura crudele, Shampoo cerca
                  in tutti i modi di eliminare chi ostacola il suo
                  amore per Ranma-kun, per esempio prova a far
                  perdere ad Akane ogni ricordo di Ranma grazie ad
                  una sua tecnica segreta di lavaggio del cervello
                  (anzi della testa) con uno "shampoo" speciale a
                  base di una medicina cinese in grado di
                  manipolare i ricordi delle persone.</p>
                  <p>Un giorno, distrutta dalla scoperta del
                  segreto di Ranma e dopo aver (momentaneamente)
                  perso ogni speranza di accasarsi con lui, Shampoo
                  torna con Obaba al campo di allenamento di
                  Jusenkyo, e durante l'allenamento, viene spinta
                  nella Maoniichuan, cioè la sorgente del gatto
                  affogato. Shampoo si rende conto a questo punto
                  che, nonostante tutto, Ranma è un vero uomo e
                  dato che la tradizione del suo clan vuole che le
                  amazzoni debbano sempre sposare un uomo forte,
                  torna con Obaba in Giappone per cercare con tutti
                  i mezzi, leciti e non, di sposarsi con Ranma.
                  Shampoo fa anche la cameriera al ristorante Neko
                  Hanten e consegna "ramen" a domicilio in
                  bicicletta, investendo persone e volando sui
                  tetti!</p>
