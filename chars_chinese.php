            <p>Anche se non tutti sono una famiglia nel senso
            "anagrafico" del termine, esiste in Ranma una "gang" di
            cinesi nutrita e molto assortita (nonché importante per
            la serie). Questo è normale dal momento che in Ranma
            tutto ha origine dalla Cina...</p>
