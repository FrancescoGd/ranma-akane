                  clan e che si sono allenate molto duramente in
                  tecniche di arti marziali ridicole da quando
                  hanno visto Ranma battere Shampoo.</p>
                  <p>Sono arrivate in Giappone solo per chiedere a
                  Shampoo come ha ucciso Ranma e, quando scoprono
                  che non l'ha fatto, ci provano loro. Shampoo le
                  ferma e dà loro il famoso "bacio della morte",
                  rattristandole perché ha tradito la sua gente.
                  Alla fine, però, sembra che anche loro si
                  innamorino del povero Ranma, ancora una volta
                  involontario rubacuori.</p>
