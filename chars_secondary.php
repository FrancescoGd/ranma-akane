                    disse a Genma che sua figlia voleva sposare
                    Ranma, ma Genma gli risponde che Ranma ha già una
                    fidanzata. Quando sente questo, il padre di Ukyo,
                    rattristato, si lascia sfuggire il fatto che
                    avrebbe dato a sua figlia lo yatai in dote, così
