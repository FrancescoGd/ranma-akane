                <td valign="top" class="datatable-text">
                  <p>Cirrca 10 anni prima, mentre era in giro per
                  il paese ad allenarsi con il padre, Ranma rubava
                  quotidianamete le "<em>okonomiyaki</em>" (una via
                  di mezzo tra una focaccia e un'omelette) dallo
                  "<em>yatai</em>" (chiosco di cibo) del padre di
                  Ukyo, vincendo facilmente le difese di "Ucchan".
                  Comunque era una rivalità amichevole e per gioco
                  e Ukyo faceva dei disegni con le salse sopra le
                  okonomiyaki per Ranma. Un giorno il padre di Ukyo
                  disse a Genma che sua figlia voleva sposare
                  Ranma, ma Genma gli risponde che Ranma ha già una
                  fidanzata. Quando sente questo, il padre di Ukyo,
                  rattristato, si lascia sfuggire il fatto che
                  avrebbe dato a sua figlia lo yatai in dote, così
                  Genma cambia idea e gli promette che avrebbero
                  preso Ukyo con loro. Genma chiede quindi a Ranma
                  di decidere e dirgli chi preferisce tra Ukyo e le
                  okonomiyaki. Ovviamente Ranma, un po' perché era
                  ancora bambino e un po' perché non sospettava
                  minimamente che Ukyo fosse una ragazza, decide
                  per le okonomiyaki, e così Genma e Ranma si
                  prendono lo yatai e scappano via lasciandosi
                  dietro Ukyo. Derisa dalle sue compagne perché
                  abbandonata dal "fidanzato", Ukyo decide che non
                  si sarebbe mai più innamorata di un ragazzo in
                  vita sua e comincia a vestirsi e a vivere da
                  uomo, dedicandosi alle "arti marziali
                  culinarie".</p>
                  <p>Qualche anno più tardi si presenta come nuovo
                  studente alla scuola Furinkan e sfida Ranma. Dopo
                  un'avvincente duello, Ranma scopre il vero sesso
                  di Ukyo (oltretutto pensando che sia caduta anche
                  lei nella sorgente, cerca di farla tornare uomo
                  con l'acqua calda!). Ukyo invece scopre che Ranma
                  non va molto daccordo con la sua attuale
                  fidanzata "senza fascino". I due fanno pace e
                  Ukyo si innamora di nuovo di Ranma. Nonostante
                  Ukyo adesso sogni di sposare Ranma e cerchi di
                  separarlo da Akane, Ranma la considera invece
                  soltanto come un vecchio amico. I suoi tentativi
                  di prendersi Ranma, sono comunque meno violenti
                  di quelli delle altre pretendenti di Ranma: ad
                  esempio, invece di provare ad uccidere Akane,
                  Ukyo cerca di farla mettere insieme a Ryoga.</p>
                  <p>Nel frattempo Ukyo ha anche aperto un negozio
                  di okonomiyaki chiamato "Ucchan" che le permette
                  di essere un'esperta sia di lavori domestici sia
                  in affari, e anche se ora vive per cucinare
                  okonomiyaki, sarebbe disposta a dare tutto via
                  pur di sposare Ranma.</p>
