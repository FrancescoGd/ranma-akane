            <p>In questa sezione vengono presi in esame quei
            personaggi che sono in apparenza secondari, ma si sono
            ritagliati un proprio spazio non indifferente
            all'interno di tutta la narrazione diventando, in certi
            casi, addirittura i protagonisti di alcune situazioni
            (come Ryoga e Ukyo, per esempio).</p>
