                <td valign="top" class="datatable-text">
                  <p>È il vecchio pervertito della situazione! È un
                  uomo anziano ma anche molto molto potente,
                  grandissimo esperto di arti marziali ed è stato
                  il sensei di Genma e Soun i quali lo temono e lo
                  detestano allo stesso tempo. Anni fa egli li
                  allenò duramente, facendoli anche partecipare ai
                  suoi saccheggi di biancheria intima femminile e
                  punendoli quando rimanevano indietro. Alla fine,
                  essi riescono a farlo ubriacare, incatenarlo e
                  chiuderlo dentro un barile, gettare il barile in
                  una caverna sigillandola con la dinamite e con
                  una pietra con delle incisioni Buddhiste sopra.
                  Un giorno Happosai, che tutti ormai credevano
                  morto, riesce a liberarsi e si presenta al dojo
                  Tendo con la pretesa di allenare il suo
                  successore: molto vigliaccamente Genma gli
                  presenta Ranma come suo succesore.</p>
                  <p>Happosai è un pervertito feticista al 100%
                  (anche di più) e se ne va in giro rubando
                  biancheria intima femminile per la sua grande
                  collezione e palpando ragazze. Le sue perversioni
                  sono anche l'origine della sua forza: se rimane
                  troppo tempo senza toccare un corpo femminile o
                  della biancheria intima la perde. Akane e
                  Ranma-chan sono ovviamente le ragazze che
                  preferisce.</p>
