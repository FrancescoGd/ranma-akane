                <td valign="top" class="datatable-text">
                  <p>L'eterno disperso, col braccio d'acciaio ed il
                  cuore di vetro (nonché il personaggio più
                  rompiscatole di tutta la serie). Ryoga e Ranma
                  sono rivali sin da quando andavano a scuola
                  insieme da bambini. A quel tempo Ryoga sfidò
                  Ranma ad un combattimento da tenersi in un prato
                  dietro la sua casa ma, mentre vi si dirigeva
                  (doveva solo girare l'angolo di casa sua) si
                  perse e girò in lungo e in largo il Giappone per
                  quattro giorni! Ranma lo aspettò ma, dopo quattro
                  giorni di inutile attesa, tornò a casa e partì
                  col padre per un viaggio di allenamento. Per
                  riscattare l'offesa subita, Ryoga lo seguì fino
                  in Cina dove una ragazza col codino ed un panda
                  che si inseguivano, lo buttarono nella sorgente
                  Heituenniichuan, cioè quella del "Porcellino Nero
                  Affogato". Il panda dopo averlo buttato in acqua,
                  salvò il "nuovo" porcellino dall'affogamento...
                  ma soltanto per farselo cucinare dalla guida
                  cinese! La guida buttò il porcellino in un
                  pentolone d'acqua bollente dal quale uscì quindi
                  Ryoga che riuscì così a salvarsi dalla bollitura
                  (purtroppo ^__^;).</p>
                  <p>Ryoga odia con tutte le sue forze Ranma,
                  responsabile della sua maledizione; consapevole
                  della sua colpa, Ranma decide di non rivelare a
                  nessuno il segreto di Ryoga. Akane decide di
                  prendere con sé il porcellino (non conoscendo la
                  sua vera identità) e di chiamarlo "P-Chan". Ryoga
                  si innamora profondamente di Akane (il maledetto
                  &gt;:-]) anche se è convinto che nessuna ragazza
                  si innamorerà mai di lui a causa della
                  maledizione. Nonostante gli svariati "messaggi" e
                  "segnali" che riceve (soprattutto da parte di
                  Ranma), Akane non si è mai accorta del fatto che
                  P-Chan fosse in realtà Ryoga, e anzi lo difende
                  ogni volta che Ranma cerca di dargli fastidio,
                  perché pensa sia impossibile che un ragazzo sia
                  geloso di un maialino. Possiamo comunque dire
                  che, dopotutto, Ryoga è il migliore amico di
                  Ranma.</p>
                  <p>Oltre a non sapersi orientare minimamente,
                  Ryoga viene anche ingannato dai più ridicoli
                  travestimenti di Ranma. È comunque estremamente
                  forte ed abile nelle arti marziali e, a volte,
                  distrugge rocce e muri inavvertitamente quando è
                  agitato.</p>
