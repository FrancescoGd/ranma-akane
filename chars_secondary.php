                <td valign="top" class="datatable-text">
                  <p>Il chiroterapeuta della storia è un grande
                  esperto di medicina delle arti marziali, almeno
                  quando non è fuori di sé! Infatti, ogni volta che
                  vede Kasumi o la sente soltanto nominare, esce
                  completamente fuori di testa. I sintomi della sua
                  follia sono: appannamento degli occhiali,
                  sbagliare cose elementari come le porte delle
                  stanze o il verso della cornetta del telefono, e
                  il cominciare a comportarsi come un perfetto
                  idiota (ad esempio ballare in giardino
                  abbracciato al suo scheletro "Betty-chan") e così
                  via; quando il Dottore è in questo stato, diventa
                  un autentico pericolo per sé stesso e per i suoi
                  pazienti. Ovviamente tutti quanti sanno del suo
                  amore per Kasumi, tranne la diretta interessata e
                  lui!</p>
                  <p>All'inizio della serie, vediamo che anche
                  Akane è segretamente innamorata di Tofu,
                  soffrendo del fatto che questi invece ama Kasumi.
                  Il motivo per cui all'inizio della serie Akane
                  porta i capelli lunghi, è proprio per "spirito
                  d'emulazione" verso la sorella, per cercare di
                  essere più femminile e piacere al Dottore: nel
                  corso della serie, comunque, questo amore sembra
                  sparire progressivamente per essere rimpiazzato
                  da quello verso Ranma. A volte Tofu fa anche da
                  consigliere per Akane e Ranma.<br>
                  Genma è riuscito a farsi assumere part-time nello
                  studio del Dottore come aiutante tuttofare
                  (notare che lui solitamente lavora lì sotto forma
                  di panda... e nessuno sembra considerare strana
                  questa cosa!!).</p>
