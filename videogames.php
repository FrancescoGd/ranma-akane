            <h2 class="mt-5">PlayStation</h2>

            <div class="card m-3 mb-3 border-danger">
              <div class="card-header volume-card-header text-center">Ranma ½ - Battle Renaissance</div>
              <div class="card-body">
                <div class="card-title">Rumic Soft - 1996</div>
                <div class="card-subtitle mb-2 text-body-secondary">Sony PlayStation 1 (NTSC-J)</div>
                <div class="row">
