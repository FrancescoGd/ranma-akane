            <p>Questo gioco è abbastanza carino, dovete spingere
            dei blocchi in giro per lo schermo ed evitare i nemici
            (tutti tranne Akane). Quando un blocco viene spinto da
            Ranma-kun, vola attraverso tutto lo schermo; se viene
            spinto da Ranma-Chan, si sposta di un solo posto.
            Bisogna anche tenere d'occhio il Panda Genma che gira
            lanciando acqua. Per ogni livello, bisogna raccogliere
            tre oggetti (un berretto, un nastro ed un paio di
            guanti) che sono nascosti all'interno dei blocchi che
            vanno quindi spaccati per cercare. Dopo aver raccolto
            tutti e tre gli oggetti, per passare il livello,
            bisogna incontrarsi con Akane che si aggira per il
            quadro.</p>
