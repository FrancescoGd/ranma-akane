            <p>Si tratta del classico gioco sullo stile di Columns
            e Dr. Mario. Ci sono quattro diversi tipi di "mosse":
            roccia, forbici, carta e "bolle", come nella morra.
            L'idea è semplice: mettere una roccia sopra le forbici,
            fa vincere la roccia ecc. Ogni volta che viene
            scatenata una reazione, le bolle adiacenti scompaiono.
            Dopo un certo numero di "reazioni", il personaggio
            esegue una mossa speciale (ad esempio Ranma esegue la
            "kachu tenshin amaguriken").</p>
            <p>Ci sono sei personaggi: Ranma, Akane, Ryoga,
            Shampoo, Mousse e Genma. Ogni personaggio ha diversi
            tipi di attacchi e velocità che gli danno vantaggi e
            svantaggi. Il Re del Casinò, è il boss finale.</p>
