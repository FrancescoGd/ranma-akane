            <p>In questa pagina cerco di raccogliere informazioni
            su tutti (spero, ne mancano ancora diversi all'appello)
            i videogiochi che hanno per protagonista il nostro
            eroe.</p>
            <p>&nbsp;</p>
            <p>Dal momento che purtroppo nessuno è stato mai
            distribuito ufficialmente in Italia, sono sempre
            risultati un po' difficili da reperire, se non tramite
            negozi che effettuano importazione o con acquisti
            online, cosa che ne determina anche il prezzo
            abbastanza alto (spesso ai limiti della rapina, ma
            questo non riguarda soltanto Ranma).</p>
            <p>&nbsp;</p>
            <p>I più facilmente recuperabili sono quello per la
            Playstation 1 perché è il più recente (ma anche uno dei
            meno belli purtroppo) e quelli per il Super Famicom
            (Super Nintendo), di questi ultimi però capita di
            vedere sempre i soliti 2 o 3.</p>
            <p>&nbsp;</p>
            <p>Come sempre accade in questi casi non tutti i
            videogiochi su Ranma sono eccezionali e alcuni sono
            esclusivamente un pretesto commerciale. Altri, invece,
            sono davvero belli e ben fatti.</p>
