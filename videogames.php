            <p>Probabilmente è il miglior videogioco dedicato a
            Ranma, non solo sul NES ma in assoluto su ogni
            piattaforma. La grafica è la migliore tra tutti quelli
            della serie di combattimento. Anche se un po' più lenta
            rispetto ai primi due episodi, questo gioco li
            surclassa in tutti gli altri aspetti.</p>
            <p>Avete a disposizione 12 personaggi: Ranma-kun,
            Ranma-chan, Ryoga, Mousse, Shampoo, Ukyo, Akane,
            Mariko, Genma Panda, Tatewaki, Kodachi e Hinako. Il
            boss di fine livello è Herb. Ogni personaggio ha due o
            tre mosse speciali ed un super attacco che può essere
            sferrato quando il livello di energia raggiunge il
            massimo.</p>
            <p>Potete selezionare anche lo story mode, in cui ogni
            personaggio deve portare a termine una missione
            differente. Davvero notevole e raccomandato.</p>
