                <p class="card-text">Inizio da questo perché è quello più recente che ho (anche perché è l'ultimo gioco
                uscito ad essere esclusivamente dedicato a Ranma).
                Ovviamente, essendo un gioco per Playstation, ci si
                aspetta di trovarsi davanti un gioco strabiliante e
                graficamente eccezionale per i tempi: purtroppo devo dire che non è
                così. Nonostante il gioco sia stato realizzato dalla
                Rumic Soft, che pure sulle consolles a 8 bit aveva
                tirato fuori dei giochi davvero piacevoli su Ranma,
                devo dire di essere rimasto un po' deluso da questa
                versione per la PSX.</p>
                <p class="card-text">Come tipologia di gioco ci troviamo di fronte al
                classico picchiaduro 3D, quindi un gioco alla maniera
                di Tekken o Virtua Fighter. Peccato che Ranma non possa
                competere con le due più famose pietre di paragone.</p>
                <p class="card-text">La storia del gioco ricorda vagamente quella del
                secondo film <em>"La sposa dell'isola delle illusioni"</em> e ci
                viene rappresentata nel filmato introduttivo. Tutta la
                combriccola dei personaggi (i Tendo, i Saotome,
                Happosai, Kuno, Ryoga ecc.) si ritrovano insieme a fare
                una gita su un'isola di proprietà della Famiglia Kuno.
                A quanto pare sull'isola stanno effettuando dei lavori
                per costruire un luna park. Ad un certo punto, nel bel
                mezzo della gita, ai nostri appare un Oni (demone) che
                lancia una specie di anatema. Qui si conclude il primo
                filmato.</p>
                <p class="card-text">Come in ogni gioco che si rispetti, abbiamo la
                possibilità di scegliere tra diverse possibilità di
                gioco, cioè il duello singolo oppure lo "story mode"
                alla Tekken. Qualunque modalità scegliamo, abbiamo poi
                la possibilità di decidere con quale personaggio
                giocare. Ovviamente in base al personaggio abbiamo a
                disposizione mosse e caratteristiche diverse e, se
                abbiamo scelto lo story mode, ogni personaggio ha una
                missione differente da compiere: ad esempio se
                scegliamo Ranma, dobbiamo salvare Akane che è stata
                rapita dall'oni.</p>
                <p class="card-text">Fondamentalmente, le cose che non mi sono piaciute
                in questo gioco sono due. La prima è la grafica.
                Nonostante le potenzialità della macchina siano
                notevoli, evidentemente queste non sono state sfruttate
                a dovere. La grafica risulta un po' troppo squadrata e
                con animazioni a volte "scattose" per quanto riguarda i
                personaggi. Gli sfondi, a parte un paio, sono un po'
                troppo approssimativi, anche se le idee sono belle e
                divertenti (tipo quella in cui si vede una radura con
                le statue del Preside Kuno in stile "Isola di Pasqua"
                ^__^). La seconda è il livello di difficoltà degli
                incontri che secondo me è bilanciato male: mettendo la
                modalità più facile, il gioco risulta un po' troppo
                semplice da portare a termine. Viceversa, la modalità
                difficile è veramente dura. Insomma, o lo finite in 10
                minuti, oppure in 1 mese ^^;;</p>
                <p class="card-text">C'è anche qualcosa che mi è piaciuto, fortunatamente.
                Innanzitutto i filmati iniziali, finali e le scenette
                di intermezzo: sono davvero belle e fanno morire dal
                ridere (in particolare una in cui si vede Happosai,
                mezzo ubriaco, che saltella da una parte all'altra con
                una bottiglia di sakè in mano). Poi c'è il fatto che
                ogni personaggio ha le sue mosse speciali uguali a
                quelle del manga: ad esempio, se scegliete Ranma,
                potrete usare l'hiryu-shoten-ha, se scegliete Ryoga la
                bakusai-tenketsu e così via. Poi, al contrario di
                quanto succede in Tekken, quando un personaggio fa una
                mossa, urla il suo nome come accade nel manga e
                nell'anime: sentirete quindi Happosai urlare: "Happooo
                Daikariiin!!!" e colpire il nemico. Davvero bello. Poi
                anche le musiche non sono affatto male. Un'altra cosa
                simpatica, è che i personaggi "trasformabili", possono
                ricevere della pioggia durante i combattimenti e quindi
                combattere con la loro seconda identità: combattere col
                panda è uno spasso. Ogni personaggio ha più o meno lo
                stesso carattere che ha nel manga: è quindi normale
                vedere Tatewaki che, durante il combattimento, si ferma
                e con tono serioso dice: "Io sono il Tuono Blu..." con
                la sua aria da mezzo idiota.</p>
                <p class="card-text">Insomma, in definitiva, sono abbastanza contento di
                questo gioco, anche se mi sarei aspettato qualcosa di
                più. Ovviamente al vero fan non dovrebbe mancare, se
                non altro per le scene animate e le musiche inedite.
                Personalmente, poi, trovo un gusto particolare nel
                picchiare sanguinosamente P-Chan &gt;:-]</p>
                <p class="card-text">Per chi non è un fan di Ranma risulta invece un
                gioco non entusiasmante.</p>
                <p class="card-text">Dato che il booklet del CD è in giapponese, qui di
                seguito potete scaricarvi una piccola FAQ (in inglese)
                sulle mosse di ogni personaggio e le combinazioni di
                tasti per ottenerle.</p>
