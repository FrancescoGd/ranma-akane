				<p> Rumiko Takahashi           (la pi&ugrave; famosa e pi&ugrave; tradotta autrice donna di fumetti,           con oltre 50 milioni di copie stampate dei suoi volumi), &egrave; nata           nel 1957 a Nigata.</p>
				<p>I suoi primi passi da mangaka ha cominciato a muoverli           alla scuola di <em>Kazuo Koike</em>, l'autore dei testi di <em>"Crying Freeman"</em>.</p>
				<p>Le sue influenze principali includono il folklore, le novelle di <em>Yasutaka           Tsutsui</em> ed i comics americani <em>Marvel</em> per quanto riguarda l'azione.</p>
