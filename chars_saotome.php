                  <p>È l'insensibile padre di Ranma. Molto tempo
                  fa, si mise daccordo col suo amico Soun Tendo,
                  affinché i loro due figli si sposassero. Mentre
                  stava allenandosi con Ranma nello sperduto Campo
                  di Jusenkyo in Cina, è stato spinto dal figlio
                  dentro la Sorgente Shonmaoniichuan, da dove è
                  uscito trasformato in Panda. Al contrario di
                  Ranma, Genma non ha molti problemi a convivere
                  con la sua doppia natura e, anzi, a volte gli
                  piace vivere da panda, soprattutto se ciò gli
                  torna utile. Anche quando è un panda, continua a
                  fare alcune cose come se fosse un uomo, come, ad
                  esempio giocare a shogi con Soun. Dal momento che
                  il panda non può parlare, si esprime tramite dei
                  cartelli di legno che appaiono praticamente dal
                  nulla!</p>
