                  <p>L'influenza di una madre con le sue
                  preoccupazioni, è dannosa per gli allenamenti di
                  un ragazzo nelle arti marziali, così Genma prende
                  suo figlio e lascia Nodoka. Quando parte, Genma
                  giura di riuscire a fare di Ranma un uomo
                  completo, altrimenti avrebbe fatto un onorevole
                  "seppuku" (quello che noi chiamiamo hara-kiri).
                  Come moglie di un maestro di arti marziali,
                  Nodoka accetta la decisione di Genma.</p>
                  <p>Nodoka riceve regolarmente delle lettere da
                  Genma, finché questi arriva a Jusenkyo con Ranma
                  e le lettere smettono di arrivare. Un giorno
                  Nodoka arriva al dojo dei Tendo con lo scopo di
                  rivedere suo figlio cresciuto e diventato uomo e,
                  se così non fosse, ricordare a Genma la sua
                  promessa! Così quando sente delle voci
                  riguardanti suo figlio che si veste da donna e
                  cose del genere, la sua tranquillità viene meno e
                  impugna la katana. Quando finalmente arriva al
                  dojo, Ranma e Genma sono "partiti per un altro
                  viaggio di allenamento", lasciando la fidanzata
                  Akane con sua "cugina Ranko" e il suo "Panda
                  domestico" (credo sia inutile precisare chi siano
                  la "cugina" e il "panda domestico"...).</p>
