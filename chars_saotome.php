                  ritmica marziale, pattinaggio artistico marziale,
                  cerimonia del tè marziale, arti culinarie
                  marziali e così via).</p>
                  <p>Mentre si allenava a Jusenkyo con suo padre, è
                  caduto nella Nyanniichuan subendone la
                  maledizione.</p>
