                    <h5 class="card-title">Saotome Ranma (早乙女 乱馬)</h5>
                    <p class="card-text">È il nostro "eroe" ed il <em>Kenpō</em> è la sua vita.
                    Non rifiuta mai una sfida e, soprattutto se ha
                    "marziale" nel nome, la vince pure! (ginnastica
                    ritmica marziale, pattinaggio artistico marziale,
                    cerimonia del tè marziale, arti culinarie
                    marziali e così via). Nonostante la sua aria scanzonata e la giovane età (16 anni)
                    Ranma è comunque un eccellente artista marziale ed una delle sue principali capacità
                    è quella di riuscire ad imparare nuove tecniche molto in fretta, cosa che lo aiuterà
                    ad uscire da non poche situazioni.</p>
                    <p class="card-text">Mentre si allenava a Jusenkyo con suo padre, è
                    caduto nella Nyanniichuan subendone la
                    maledizione.</p>
                    <p class="card-text">A seguito di un accordo tra genitori, è stato
                    deciso che Ranma dovrà sposare Akane Tendo e
                    portare avanti con lei la tradizione del dojo, ma
                    nessuno dei due vuole rispettare questo
                    accordo... almeno apparentemente!<br>
                    Nella realtà, anche se non lo ammetteranno mai esplicitamente soprattutto davanti agli altri,
                    col proseguire della storia diventa evidente che entrambi hanno iniziato a coltivare un fortissimo
                    sentimento e attaccamento l'uno per l'altra e, insomma, possiamo dire senza tema di
                    smentita che il loro è vero amore (anche se a modo loro, ma il bello è anche lì!).<br>
                    La cosa è evidente anche dal fatto che non di rado ognuno dei due è sempre pronto ad intervenire
                    per salvare o coprire l'altro, che sia in un combattimento o in una situazione difficile da gestire.</p>
