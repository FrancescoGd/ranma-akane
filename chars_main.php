            <p>I personaggi presenti in Ranma ½ sono tantissimi
            e, caratteristica della Takahashi presente non solo in
            Ranma, sono tutti perfettamente caratterizzati e con
            una propria psicologia ben delineata: anche quelli più o meno
            marginali hanno sempre una loro personalità peculiare.
            Elencarli tutti è un'impresa nel vero senso
            della parola poiché ne sono apparsi continuamente di
            nuovi, alcuni appaiono solo una volta e, oltretutto, ci
            sono alcune differenze tra il Manga, la serie TV ecc.
            Cercherò quando riesco di sottolineare queste
            differenze.</p>
            <p>L'elenco è suddiviso, ove possibile, per <em>"famiglia"</em> di
            appartenenza o per provenienza. Oltretutto, all'interno
            delle famiglie, i personaggi sono elencati in ordine di
            "albero genealogico" (per questo non troverete Ranma o
            Akane per primi) oppure di apparizione nella serie.</p>
            <p>Relativamente ai doppiatori ho preferito, anziché inserirli nelle schede dei singoli personaggi,
              raggrupparli tutti in questa pagina in modo da avere una comoda tabella comparativa tra le varie
              edizioni (<a href="#Doppiatori">questa qui</a>).
            </p>
            <p><strong class="highlight_section">Nota sull'anime del 2024:</strong> dato che nel momento in cui ho iniziato ad aggiornare
            queste pagine il nuovo anime è ancora in corso di trasmissione, le immagini con il nuovo <em>character
            design</em> le aggiungo mano a mano che i relativi personaggi fanno la loro apparizione nella serie (anche nelle versioni trasformate per cui, per esempio,
            ora c'è Shampoo versione 2024 ma non Shampoo gatta versione 2024), così come
            ovviamente l'annotazione sull'episodio di "debutto" e il doppiatore.</p>
