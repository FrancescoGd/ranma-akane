                                <p align="left">&nbsp;</p>
                                <p align="left" class="faq_reply">(Ni'uh`em'anm'aor'enn`iquan)</p>
                                <p align="left">&nbsp; </p>
                                <p align="left" class="faq_reply"><strong>Sorgente del Toro, della Gru, dell'Anguilla e dello Yeti affogati</strong></p>
                                <p>&nbsp;</p>
                                <p class="faq_reply">2500 anni fa, uno Yeti che cavalcava un Toro,       mentre portava una Gru e un'Anguilla, affog&ograve; in questa sorgente. Happosai       inizi&ograve; Taro in questa sorgente, trasformandolo in un bambino-mostro       dall'aspetto di un minotauro alato e con una coda a forma di serpente.</p>
