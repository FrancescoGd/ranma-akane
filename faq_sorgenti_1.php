                                        <p align="left" class="faq_reply"><strong>Sorgente dell'Anatroccolo Selvatico affogato</strong></p>
                                        <p align="center">&nbsp;</p>
                                        <blockquote>
                                            <p class="faq_reply">1300 anni fa, un Anatroccolo Selvatico affog&ograve;       in questa sorgente. Forse era stanco di nuotare! Mousse arriv&ograve; qui       per allenarsi dopo </p>
                                            <p class="faq_reply">essere stato battuto da Ranma e, dato che non ci vede molto       bene, camminando fin&igrave; detro questa sorgente. Si riport&ograve; indietro       un po' di quell'acqua e cerc&ograve; di bagnarci anche Ranma e Akane.</p>
                                            <p>&nbsp;</p>
                                            <p> <span class="faq_reply"><strong class="highlight_section">Apparizione:</strong> Nettohen episodio 13. Volume 10       parte 5. </span></p>
