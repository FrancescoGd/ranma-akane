				<h2>Jusenkyo/Chochuanshan</h2>
				<p align="center"><strong><br />
				    La Valle delle Sorgenti Maledette</strong></p>
				<p>&nbsp; </p>
				<blockquote>
                    <p>Sulle montagne sperdute Pa-yen-k'a-la, nella       regione cinese di Tsinghai, si trova il campo di allenamento di Jusenkyo (o       Chochuanshan, come lo chiamano i cinesi). Vi si trovano pi&ugrave; di 100       sorgenti, ognuna associata ad una tragica legenda.</p>
				    <p>Quelli che cadono in una delle sorgenti ne       subiscono la maledizione: l'acqua fredda li trasforma in ogni sorta di creatura       affogata l&agrave; dentro secoli prima, mentre l'acqua calda li fa ritornare normali.</p>
				    <p>&nbsp;</p>
				    <h2>Le Sorgenti di Jusenkyo</h2>
				    <p>&nbsp;</p>
				    <h4>Shonmaoniichuan</h4>
				    <p align="center">&nbsp;</p>
				    <p class="faq_reply">(Xi'ongm^aon`iquan)</p>
				    <p>&nbsp;</p>
				    <p class="faq_reply"><strong>Sorgente del Panda Affogato</strong></p>
				    <p align="center">&nbsp;</p>
				    <blockquote>
                        <p class="faq_reply">2000 anni fa, un Panda affog&ograve; in questa       sorgente. Ranma scaraventa Genma dentro questa sorgente durante un allenamento.</p>
                        <p>&nbsp;</p>
                        <p> <span class="faq_reply"><strong class="highlight_section">Apparizione:</strong> Episodio 1. Volume 1 parte 2. </span></p>
				        <p>&nbsp;</p>
				        <p>&nbsp;</p>
				        <h4>Nyanniichuan</h4>
				        <p>&nbsp;</p>
				        <p align="left" class="faq_reply">(Ni'angn`iquan)</p>
				        <p align="left">&nbsp; </p>
				        <p align="left" class="faq_reply"><strong>Sorgente della Ragazza Affogata</strong></p>
				        <div align="left">
				            <blockquote>&nbsp; </blockquote>
				            </div>
				        <blockquote><p align="left">&nbsp;</p>
                            <p class="faq_reply">1500 anni fa, una Ragazza affog&ograve; in       questa sorgente. Mentre Ranma era distratto dalla trasformazione di Genma       in Panda, Genma lo getta a sua volta in questa sorgente.</p>
                            <p class="faq_reply">Un giorno Herb, della       "Trib&ugrave; del Muschio", ha gettato una scimmia dentro questa       sorgente per procurarsi una sposa. Ma mentre Herb era distratto perch&eacute;       vedeva un seno nudo per la prima volta in vita sua, la ragazza-scimmia lo       ha tirato gi&ugrave; nella stessa sorgente. </p>
                            <p>&nbsp;</p>
                            <p> <span class="faq_reply"><strong class="highlight_section">Apparizione:</strong> Episodio 1. Volume 1 parte 2. </span></p>
                            <p>&nbsp;</p>
                            <p>&nbsp;</p>
                            <h4>Heituenniichuan</h4>
                            <p>&nbsp;</p>
                            <p align="left" class="faq_reply">(H^eit'unn`iquan)</p>
                            <p align="left">&nbsp; </p>
                            <p align="left" class="faq_reply"><strong>Sorgente del Porcellino Nero Affogato</strong></p>
                            <blockquote>
                                <p>&nbsp;</p>
                                <p class="faq_reply">1200 anni fa, un Porcellino Nero affog&ograve;       in questa sorgente. Mentre Ryoga stava cercando Ranma sulle montagne cinesi,       una "certa" ragazza arrabbiata che stava inseguendo un "certo"       panda, l'ha spinto gi&ugrave; da una collina dentro questa sorgente. </p>
                                <p>&nbsp;</p>
                                <p> <span class="faq_reply"><strong class="highlight_section">Apparizione:</strong> Episodio 10. Volume 2 parte       8.</span></p>
