            <h2>Jusenkyo / Chochuanshan</h2>
            <p align="center"><strong>La Valle delle Sorgenti Maledette</strong></p>
            <blockquote>
              <p>Sulle montagne sperdute <em>Pa-yen-k'a-la</em>, nella
              regione cinese di <em>Tsinghai</em>, si trova il campo di
              allenamento di <em>Jusenkyo</em> (o <em>Chochuanshan</em>, come lo
              chiamano i cinesi). Vi si trovano più di 100
              sorgenti, ognuna associata ad una tragica
              legenda.</p>
              <p>Quelli che cadono in una delle sorgenti ne
              subiscono la maledizione: l'acqua fredda li trasforma
              in ogni sorta di creatura affogata là dentro secoli
              prima, mentre l'acqua calda li fa ritornare
              normali.</p>

