                      <p class="faq_reply">Qualche mese fa, un uomo
                      ed una rana affogarono in questa sorgente.
                      Kaeru Sennin, l'ammaestratore di rane, disse
                      che si trovava a Jusenkyo per allenare una
                      rana a sedersi su una canna di bambù. Un
                      ragazzo, mentre stava attaccando un panda (il
                      ragazzo e il panda non sono "quelli" dell'ep.
                      1) tolse via le canne da sotto di loro.
                      L'uomo e la rana caddero dentro la sorgente e
                      ne venne fuori un uomo-rana.</p>

