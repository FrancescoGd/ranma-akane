                    <h2 class="mt-5">Altre Sorgenti</h2>

                    <h4 class="mt-5">Nanniichuan Giapponese</h4>

                    <blockquote>
                      <p class="faq_reply">Ryoga trova una mappa
                      che porta alla sorgente Nanniichuan
                      Giapponese. Secondo la mappa, molti anni fa,
                      un villaggio fu devastato da alcune volpi. Un
                      grande sacerdote cinese batté il suo bastone
                      in terra e fece sgorgare la Nanniichuan
                      Giapponese. Le volpi furono imprigionate
                      nella sorgente e trasformate in uomini.
                      Comunque questo non cambiò il loro cuore e
                      quindi saccheggiarono il villaggio anche come
                      uomini. Purtroppo la Nanniichuan Giapponese
                      non funziona più.</p>

                      <p class="faq_reply"><strong class=
                      "highlight_section">Apparizione:</strong>
                      Nettohen episodio 16. Volume 8 parte 9.</p>
                    </blockquote>


