                                <p>&nbsp;</p>
                                <p class="faq_reply"><strong>Sorgente del Gatto Affogato</strong></p>
                                <blockquote>&nbsp; </blockquote>
                                <blockquote><p>&nbsp;</p>
                                    <p class="faq_reply">1800 anni fa, un Gatto affog&ograve; in questa       sorgente. Obaba port&ograve; Shampoo al campo delle sorgenti per allenarla       a battere Ranma, e la spinse dentro questa sorgente.</p>
