                                    <p align="center">&nbsp;</p>
                                    <blockquote>
                                        <p class="faq_reply">Un giorno, Shampoo incastr&ograve; Ranma constringendolo ad       uscire con lei grazie ad un miscuglio che trasforma l'acqua normale in acqua       della sorgente Nanniichuan, e glielo dimostra trasformando un cane in un ragazzo.       Questa pozione pu&ograve; curare sia Genma che Ranma ma solo temporaneamente       perch&eacute; &egrave; una mera imitazione che funziona solo una volta.</p>
                                        <p>&nbsp;</p>
                                        <p> <span class="faq_reply"><strong class="highlight_section">Apparizione:</strong> Nettohen episodio 12. Volume       8 parte 1. </span></p>
