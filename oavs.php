            L'unico modo per salvare Shinnosuke è trovare un
            muschio speciale che cresce soltanto nella "Sorgente
            della Vita" e, per averlo, dovranno affrontare il
            guardiano della sorgente stessa, cioè un leggendario
            drago.</p>
