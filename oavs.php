            <p>La nonna di Shampoo ha una vasta collezione di
            gioielli provenienti dal villagio d'origine. Obaba
            invita Shampoo a prenderne uno, scelta che cadrà sul
            cosiddetto <em>"Gioiello del Contrario"</em> sul quale
            però non ha alcunaidea riguardo le sue potenzialità.
            Dal momento che Shampoo indossa la spilla al contrario,
            questa ha l'effetto di invertire i suoi sentimenti
            verso Ranma, facendola diventare "fredda come il
            ghiaccio" verso di lui e facendoglielo addirittura
            odiare. Confuso da questo repentino cambio di
            comportamento da parte di Shampoo, Ranma (più che altro
            per difendere il suo orgoglio maschile) prova a fare di
            tutto affinché Shampoo torni a dire che lo ama come una
            volta. Obaba, ovviamente, vuole approfittare della
            situazione per incastrare Ranma e cercare di farlo
            sposare con Shampoo. Funzionerà il suo piano?</p>
