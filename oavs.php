            <p>Se per caso avete pensato che al Liceo Furinkan le
            cose non potrebbero andare peggio, vi siete sbagliati
            perché Ninomiya Hinako viene assunta come nuova
            professoressa in <em>"Growing up with Miss
            Hinako"</em>. All'inizio, tutti si meravigliano di come
            possa una bambina diventare la loro insegnante, fino a
            quando viene scoperto che lei assorbe le loro
            <em>"aure"</em> e con l'energia acquisita può così
            trasformarsi in una donna a tutti gli effetti!!! La
            persona che rimane più impressionata da tutto ciò è
            Ranma, soprattutto quando lei usa la sua forza contro
            Happosai, riducendolo praticamente uno straccio, un
            foglio stile "sottiletta". All'inizio Ranma si danna
            per trovare la fonte della sua forza, ma poi decide di
            fermare la signorina Hinako prima che questa
            "prosciughi" le forze di tutti... soltanto che questo
            richiede lo stimolo di alcuni punti di pressione in
            "certe" aree del corpo.</p>
