            <p>Una lunga serie di furti di cibo vede Ranma e gli
            altri impegnati per trovare i colpevoli. Alla fine
            vengono scoperte <em>Kurumi</em> e <em>Natsume</em>,
            due orfane che hanno vissuto la maggior parte della
            loro vita sulla strada e volevano solo qualcosa da
            mangiare. La loro missione: ritrovare loro padre e
            pretendere l'eredità di "maestre" nella sua Scuola di
            Arti Marziali. Appena individuato il Dojo Tendo,
            comprendono due indizi sulla sua identità: una
            fotografia che lo mostra di spalle e una firma - "Soun
            Tendo". Ormai la battaglia per l'eredità della Scuola
            di Arti Marziali è cominciata, e Ranma ed Akane si
            troveranno insieme (sbruffando come sempre!) a
            difendere la proprietà del Dojo dalle "sorelle" di
            Akane.</p>
