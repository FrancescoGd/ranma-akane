            <p>Un O.A.V. (<em>Original Anime Video</em> - o anche
            OVA), è una produzione fatta direttamente per l'home
            video, cioè un prodotto generalmente di buona/ottima
            qualità che non esce né al cinema né in tv.</p>
            <p>Di Ranma ne sono stati prodotti diversi, alcuni dei
            quali davvero imperdibili (ok, per me lo sono tutti, ma
            tant'è... ^__^;;)</p>
            <p>&nbsp;</p>
