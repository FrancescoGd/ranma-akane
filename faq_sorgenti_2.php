          <div class="pagetitle">
            Domande sulle Sorgenti
          </div>
          <div class="pagecontent">
            <p>Alcune domande e risposte importanti a proposito
            delle Sorgenti Maledette di Jusenkyo.</p>
            <p>&nbsp;</p>
            <p>&nbsp;</p>
            <h4 class="c1">Come diventano "maledette" le
            sorgenti?</h4>
            <div align="left">
              <p class="faq_reply">&nbsp;</p>
              <p class="faq_reply">Probabilmente quando
              qualcuno/qualcosa "annega" in una sorgente non
              maledetta. Dopodiché, chiunque cade in quella
              sorgente (o viene bagnato con l'acqua di quella
              sorgente) subisce quella maledizione. Non si sa se
              chi dà inizo alla maledizione della sorgente debba
              per forza morirci dentro per farla diventare
              maledetta. Forse è così perché tutte le leggende
              riguardanti le pozze sono abbastanza tragiche. Nel
              Volume 37 Akane cade in una pozza, non muore
              annegata, ma la pozza diventa maledetta. Forse questo
              dipende da qualche cosa di speciale ma non viene
              specificato o indicato.</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <h4>Le persone che subiscono la maledizione sono la
              copia della persona/cosa che ha generato la
              maledizione?</h4>
              <p>&nbsp;</p>
              <p class="faq_reply">No. Ranma, Herb e la scimmia
              vengono fuori diverse. Dal momento che i personaggi
              sono dei disegni, è difficile dire, particolarmente
              per i giovani, quanto siano differenti, perciò per
              qualcuno è difficile stabilire se vi siano altre
              somiglianze oltre al fatto di essere delle giovani
              ragazze. Quando arriva la madre di Ranma, viene
              sottolineato il fatto che si assomigliano molto.
              Questo implicherebbe che Ranma-chan non è un clone
              della ragazza annegata ma è "basata" sul suo corpo
              maschile. In altre parole potremmo dire che, se Ranma
              avesse una sorella, molto probabilmente
              assomiglierebbe a Ranma-chan. La pozza di Akane è un
              caso speciale, vedete la domanda su come funzionano
              le pozze.</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <h4>Come funzionano le maledizioni?</h4>
              <p>&nbsp;</p>
              <p class="faq_reply">Questa risposta è abbastanza
              difficile perché l'evidenza è parecchio variegata. In
              linea generale potremmo dire che ogni persona,
              ovviamente, è contraddistinta da certe
              caratteristiche e le sorgenti maledette cambiano
              soltanto alcune di queste caratteristiche. Per
              esempio, Ranma ha caratteristiche molto mascoline ed
              è un ragazzo normale come altezza ecc. La maledizione
              ha cambiato solo alcune delle caratteristiche: il
              sesso in femmina e l'età un po' più giovane, il resto
              è rimasto uguale. Se ad esempio Ranma fosse stato un
              ragazzo grasso, probabilmente si sarebbe trasformato
              in una ragazza grassa. La maledizione di Genma, ha
              cambiato solo la sua specie in Panda, il suo
              carattere ed il resto sono rimasti immutati. La
              sorgente dell'uomo virtuoso, cambierebbe il sesso di
              qualcuno in maschio e la personalità in "virtuosa".
              Forse, le caratteristiche che vengono influenzate,
              sono quelle indicate nel nome della sorgente. Così,
              dato che la sorgente "iniziata" da Akane si chiama
              "sorgente di Akane annegata", avrebbe la qualità
              specifica di cambiare l'apparenza di qualcuno in
              quella di Akane, ma il resto (carattere ecc.),
              rimarrebbe probabilmente immutato.</p>
              <p class="faq_reply">&nbsp;</p>
              <p class="faq_reply">&nbsp;</p>
            </div>
            <h4>Perché è l'acqua fredda a fare il cambiamento?</h4>
            <p>&nbsp;</p>
            <blockquote>
              <p class="faq_reply">Probabilmente è solo una
              questione di espediente narrativo. È più facile,
              durante la serie, che un personaggio si imbatta in
              dell'acqua fredda. Inoltre è abbastanza comune che
              Ranma-chan si trovi in un bagno pubblico femminile,
              cambi sesso nella vasca dell'acqua calda, e tutte le
              donne lo malmenino come "pervertito". Scene simili
              sarebbero difficili da fare con un altro elemento a
              scatenare la trasformazione. Un'altra ipotesi è che
              sia così perché le sorgenti sono fredde e quindi il
              contatto con l'acqua fredda replica le condizioni
              originali della maledizione. Forse, se fossero state
              sorgenti d'acqua calda, sarebbe funzionato tutto al
              contrario.</p>
              <p class="faq_reply">&nbsp;</p>
              <p class="faq_reply">&nbsp;</p>
              <h4>A quale temperatura deve essere l'acqua?</h4>
              <p>&nbsp;</p>
              <blockquote>
                <p class="faq_reply">Forse per la trasformazione
                basta la temperatura ambiente (c.ca 20°C) mentre
                l'acqua calda sembra essere sempre molto calda. I
                personaggi a volte dicono che è bollente e si vede
                molto spesso del vapore acqueo che ne esce. Il
                limite tra caldo e freddo per la trasformazione non
                lo sappiamo, forse è la temperatura corporea.</p>
              </blockquote>
              <p class="faq_reply">&nbsp;</p>
              <p class="faq_reply">&nbsp;</p>
              <h4>Quanta acqua serve?</h4>
              <p>&nbsp;</p>
              <blockquote>
                <p class="faq_reply">Molto poca. Per esempio, nel
                Volume 34 parte 5, Happosai colpisce Ranma con una
                pistola ad acqua che conterrà al massimo 20-30ml.
                Le gocce di pioggia non hanno invece un effetto
                immediato su nessuno. Nel Volume 23, una piccola
                quantità d'acqua della sorgente dei gemelli
                colpisce il bernoccolo di Happosai, facendo uscire
                fuori un altro bernoccolo.</p>
                <p class="faq_reply">&nbsp;</p>
                <p class="faq_reply">&nbsp;</p>
                <h4>Fa differenza quale parte del corpo viene
                bagnata?</h4>
                <p>&nbsp;</p>
                <blockquote>
                  <p class="faq_reply">No (anche se, nel caso di
                  Happosai, l'effetto lo subisce il solo
                  bernoccolo).</p>
                </blockquote>
                <p class="faq_reply">&nbsp;</p>
                <p class="faq_reply">&nbsp;</p>
                <h4>Ranma può essere curato?</h4>
                <blockquote>
                  <p>&nbsp;</p>
                  <p class="faq_reply">Si, è stato detto dalla
                  stessa Takahashi. Ranma e Genma sono stati curati
                  temporaneamente dalla finta acqua di Nanniichuan
                  preparata da Shampoo, si sono tuffati nella vasca
                  del dojo e non si sono trasformati.
                  Sfortunatamente, quella era solo un'imitazione
                  che funzionava una volta sola. Ranma e Ryoga
                  trovano la Nanniichuan Giapponese, ma ormai è
                  esaurita. Dal momento che questa sorgente avrebbe
                  potuto curarli, Ranma, Ryoga, Mousse e Genma
                  vorrebbero tornare e trovarla. La sorgente
                  potrebbe curare anche Taro, ma lui è contento dei
                  suoi "poteri".</p>
                </blockquote>
                <p class="faq_reply">&nbsp;</p>
                <p class="faq_reply">&nbsp;</p>
              </blockquote>
              <h4>Perché Ranma e Genma non saltano in altre
              sorgenti finché non trovano quella dell'uomo
              affogato?</h4>
              <p>&nbsp;</p>
              <blockquote>
                <p class="faq_reply">Ovviamente perché la storia
                sarebbe stata molto meno interessante. Chiunque con
                un minimo di intelligenza non si sarebbe mai
                allenato a Jusenkyo, tanto per cominciare. Perché
                la guida di Jusenkyo non glielo ha detto subito?
                Loro non l'hanno neanche chiesto.</p>
              </blockquote>
              <p class="faq_reply">&nbsp;</p>
              <p class="faq_reply">&nbsp;</p>
              <h4>Perché Ranma-chan ha i capelli rossi nell'anime e
              neri nel manga?</h4>
              <p>&nbsp;</p>
              <blockquote>
                <p class="faq_reply">Probabilmente per "esigenze di
                copione", è più facile distinguerla da Ranma-kun.
                Nel manga, che è per la maggior parte in bianco e
                nero, Ranma-chan ha qausi sempre una specie di
                riflesso sui capelli. Sulle copertine dei tankobon
                (a colori) la differenza è più evidente. A parte
                questo, di solito le ragazze rosse hanno una certa
                reputazione di essere più "frizzanti".</p>
                <p class="faq_reply">&nbsp;</p>
                <p class="faq_reply">&nbsp;</p>
                <h4>La personalità di Ranma cambia quando diventa
                una ragazza?</h4>
                <p>&nbsp;</p>
                <blockquote>
                  <p class="faq_reply">No. Anche quando cerca di
                  comportarsi come tale, si vede. Nessuno dei
                  personaggi cambia carattere con la
                  trasformazione. Comunque, alcune sorgenti causano
                  cambi di mentalità. Nel manga, Taro va a Jusenkyo
                  per prendere l'acqua della "Sorgente dell'uomo
                  virtuoso", che avrebbe trasformato Happosai in
                  una brava persona. Nella serie Nettohen, il
                  killer che cade nella "Sorgente del monaco
                  buddhista", viene cambiato sia nel fisico che
                  nella personalità. Come detto in precedenza,
                  dipende dalle caratteristiche che vengono
                  modificate dalla particolare sorgente. La maggior
                  parte non cambiano il carattere.</p>
                </blockquote>
                <p class="faq_reply">&nbsp;</p>
              </blockquote>
              <p>&nbsp;</p>
              <h4>Cosa succede se qualcuno cade in una sorgente
              dove sono affogate più creature?</h4>
              <p>&nbsp;</p>
              <blockquote>
                <p class="faq_reply">Si trasforma in una
                combinazione delle diverse creature. Taro è stato
                battezzato nella sorgente dello yeti che cavalcava
                il toro trasportando una gru ed un'anguilla, così
                diventa una combinazione di questi. La
                Chuanshontsuniichuan (Sorgente dei gemelli)
                sembrerebbe un'eccezione. L'acqua di questa
                sorgente, raddoppia quello che tocca. Questo
                effetto suscita però un sacco di domande senza
                risposta. Cosa succede se un gemello si bagna con
                l'acqua calda e l'altro con l'acqua fredda? Se
                entrambi sono nell'acqua calda in posti diversi,
                quale scompare? La Takahashi si è mai preoccupata
                delle questioni che scatena? ^____^;;;</p>
              </blockquote>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <h4>Le "versioni trasformate" delle persone,
              crescono?</h4>
              <p>&nbsp;</p>
              <blockquote>
                <p class="faq_reply">Si. Taro fu immerso nella
                sorgente quando era bambino e si trasformava in un
                minotauro baby. Più tardi, con la crescita di Taro
                è cresciuta anche la sua controparte. Oltre a
                questo caso, dal Volume 34 parte 5, si capisce che
                il seno di Ranma-chan è cresciuto. Potrebbe anche
                darsi che questo cambi da sorgente a sorgente, ma
                non lo sappiamo.</p>
                <p class="faq_reply">&nbsp;</p>
                <p class="faq_reply">&nbsp;</p>
                <h4>Cosa succederebbe se Ranma cascasse in un'altra
                sorgente?</h4>
                <p>&nbsp;</p>
                <blockquote>
                  <p class="faq_reply">La sorgente dell'uomo
                  affogato curerebbe Ranma e Genma quindi
                  sembrerebbe che alcune sorgenti siano mutualmente
                  esclusive. D'altra parte, però, Taro torna a
                  Jusenkyo e si bagna con l'acqua della
                  Chanyuiniichuan, acquisendo così dei tentacoli.
                  Sembrerebbe quindi che altre sorgenti si
                  combinino tra loro.</p>
                </blockquote>
                <p class="faq_reply">&nbsp;</p>
                <p class="faq_reply">&nbsp;</p>
                <h4>Se Ranma-chan rimanesse incinta, cosa
                accadrebbe quando torna uomo?</h4>
                <p>&nbsp;</p>
                <blockquote>
                  <p class="faq_reply">Probabilmente, il mondo non
                  lo saprà mai ^___^ Non ci troviamo in <em>"Cream
                  Lemon"</em> e nessuno ha mai avuto rapporti
                  sessuali in questa serie.</p>
                </blockquote>
                <p class="faq_reply">&nbsp;</p>
                <p class="faq_reply">&nbsp;</p>
                <h4>I figli subiscono la maledizione dei
                genitori?</h4>
                <p>&nbsp;</p>
                <blockquote>
                  <p class="faq_reply">Per la maggior parte della
                  serie l'unico caso simile è quello degli uomini
                  della Dinastia del Muschio che gettavano gli
                  animali nella sorgente della ragazza e poi
                  bagnavano le ragazze risultanti col Chiisuiton
                  per rendere l'effetto permanente. Non è
                  specificato se la loro prole venisse
                  immediatamente influenzata dalla maledizione o
                  no. Anche gli abitanti del Monte della Fenice
                  sono stati trasformati dall'acqua di Jusenkyo e
                  sappiamo che i loro figli erano geneticamente
                  cambiati. Anche in questo caso non sappiamo se il
                  cambiamento era immediato o no.</p>
                </blockquote>
                <p class="faq_reply">&nbsp;</p>
                <p class="faq_reply">&nbsp;</p>
                <h4>Se Ryoga e Akane avessero dei bambini, si
                trasformerebbero in maialini anche loro?</h4>
                <p>&nbsp;</p>
                <blockquote>
                  <p class="faq_reply">Forse. L'episodio 45 della
                  Nettohen suggerisce che potrebbe accadere, ma si
                  trattava comunque di un sogno.</p>
                  <p class="faq_reply">&nbsp;</p>
                  <p class="faq_reply">&nbsp;</p>
                  <h4>Se Shampoo avesse dei figli, si
                  trasformerebbero in gatti?</h4>
                  <p>&nbsp;</p>
                  <blockquote>
                    <p class="faq_reply">Probabilmente no, ma se
                    avesse dei figli sarebbero probabilmente molto
                    forti ed agili (come i gatti), con capacità
                    superiori agli uomini. La tribù di Herb, Lime e
                    Mint, ritualmente, gettava animali selvatici
                    come lupi e tigri nella Nyannichuan per farli
                    diventare donne. I loro figli ereditavano
                    capacità sovraumane correlate all'animale che
                    una volta era la madre. Forse si vedrebbe un
                    po' anche esteriormente, dato che Herb, Lime e
                    Mint hanno gli artigli e le pupille da
                    animali.</p>
                  </blockquote>
                  <p class="faq_reply">&nbsp;</p>
                </blockquote>
