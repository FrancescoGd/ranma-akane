<div class="pagetitle">Benvenuto,</div>
<div class="pagecontent">
<p class="highlight_section">questo sito è il mio piccolo e
personale tributo a <strong>Ranma Saotome</strong> e <strong>Akane
Tendo</strong>, perché li considero la coppia più bella, più
simpatica, più singolare, più azzeccata e più "tutto" della storia
del fumetto e dell'animazione e perché penso che, in fondo, il loro
sia un <strong>grande amore</strong>. Quindi...<br>
<strong>Benvenuto nel <em>Mondo di Ranma e Akane</em>!</strong></p>
<p>&nbsp;</p>
<p>Innanzitutto, qualche nota sul contenuto del sito: come noterete
leggendo le varie pagine, alcune sono pagine scritte da me, e altre
sono traduzioni di pagine di altri autori, sempre dietro permesso
degli autori originali. Comunque, anche nelle pagine tradotte, si
trovano commenti o aggiunte fatti da me, quindi troverete sempre
qualche cosa di inedito e personale. Inoltre, dato che questo è un
lavoro che avevo iniziato a metà anni '90, alcuni di questi siti
hanno chiuso i battenti, perciò queste traduzioni sono tutto ciò
che rimane online del lavoro degli autori originali.</p>
