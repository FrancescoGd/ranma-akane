            Il Manga
          </div>
          <div class="pagecontent">
            <p>Il manga è iniziato nel 1987 sulle pagine della
            rivista Giapponese di fumetti <strong><em>"Shuukan
            Shonen Sunday"</em></strong>, pubblicata dalla
            <em>"Shogakukan"</em> ed è continuato per quasi dieci
            anni con una frequenza media di sedici pagine a
            settimana. Tutto quello che è uscito sul settimanale è
            stato poi raccolto in volumi chiamati
            <em>"tankobon"</em>, solitamente due mesi dopo l'uscita
            sulla rivista.</p>
            <p>&nbsp;</p>
            <p>La pubblicazione si è conclusa con il volume numero
            38 uscito nel 1997. È stato un successo su tutta la
            linea, dal momento che è tra i manga più seguiti in
            assoluto, sia in Oriente che in Occidente, seguito da
            <strong>circa dodici milioni di lettori</strong> (non è
            un dato ufficiale, comunque soltanto Shonen Sunday
            vende più di due milioni di copie!) e che per due anni
            di seguito, nel 1988 e nel 1989 (quindi dopo solo un
            anno dalla sua uscita), si è aggiudicato il titolo di
            <strong>"Migliore Manga Umoristico
            dell'Anno"</strong>.</p>
            <p>&nbsp;</p>
            <p>In Italia ha avuto invece delle uscite abbastanza
            travagliate: è iniziato dapprima su
            <em>"Mangazine"</em> della defunta "Granata Press" nel
            1992, per passare poi, nel 1995, sulle pagine della
            testata <em>"Manga Hero"</em> sempre della stessa casa
            editrice. Per ultimo, è uscito come quindicinale sulla
            testata <em>"Neverland"</em> della "Star Comics",
            composto di 53 volumetti con lettura alla occidentale,
            curato sempre dai Kappa, con una qualità (opinione
            personale) comunque, e aggiungo purtroppo, alterna e
            tendente al basso! Per ultima, sempre ad opera della
            Star Comics, è uscita una ristampa con volumetti
            suddivisi come i tankobon originali (quindi 38 numeri),
            ma con un adattamento ancora per certi versi
            discutibile.</p>
            <p>&nbsp;</p>
            <p><strong>Un paio di cose riguardo questo
            elenco:</strong> innanzitutto, noterete che alcuni
            titoli potrebbero essere differenti dalla versione
            italiana. Questo perché le traduzioni sono state fatte
            da diverse fonti. Secondo poi, la suddivisione degli
            episodi, è differente rispetto alla prima versione
            italiana, poiché (come spiegato anche nel paragrafo
            precedente) bisogna considerare che, più o meno, con un
            volume giapponese, se ne fanno un paio italiani (per
            esempio, il primo volumetto della Star, termina con
            l'episodio 5). Per ultimo, selezionando il link
            associato ad ogni numero di volume, entrerete nelle
            sinossi relative ad ogni volume.</p>
            <p>&nbsp;</p>
            <p><strong>Nota:</strong> come noterete, questa sezione
            è ancora incompleta, mancano le descrizioni degli
            ultimi quattro volumi e di alcune storie; porrò rimedio
            a questo come posso, infatti queste sinossi le
            controllo sui vari volumi in mio possesso e, quindi, mi
            ci vuole un po' di tempo.</p>
            <p>&nbsp;</p>
