            <p>Tanto per cominciare, vorrei scrivere qualche
            piccolo appunto sulle F.A.Q. e sullo "spirito" che c'è
            dietro.</p>
            <p>Innanzitutto, va precisato che le F.A.Q. sono
            <strong>basate principalmente sul manga</strong> e,
            solo occasionalmente, sull'anime esclusivamente per
            qualche domanda extra o quando si tratta di domande
            specificamente relative all'anime.</p>
            <p class="lead">Insomma, il manga è <strong>LA</strong> verità su Ranma. ^___^;;</p>
            <p>I motivi di queste scelte sono diversi, soprattutto
            il fatto che il manga è scritto direttamente da Rumiko
            Takahashi, l'anime no. Esso <span class="c1">si
            basa</span> sul manga e basta.</p>
            <p>Ecco infine qualche nota sul manga da tenere bene a
            mente:</p>

            <ol type="1">
              <li>La priorità principale è quella del divertimento.
              Sembra banale ma questa credo sia la base di tutto.
              Capita spesso che per fare del buon umorismo si debba
              cadere un po' nell'"inconsistenza", ad esempio con
              qualche lavoro precedente, qualcosa di non
              particolarmente logico e/o concreto, personaggi che
              escono un po' fuori dal personaggio ecc. ecc.</li>
              <li class="mt-2">La seconda priorità è quella di fare il
              necessario per mantenere la serie più lunga
              possibile. Quando la Takahashi l'ha pianificata,
              probabilmente aveva disegnato i personaggi principali
              già prima di qualsiasi pubblicazione, non aveva però
              scritto quasi niente delle storie, forse solo
              l'introduzione dei protagonisti e poco più (se non
              ricordo male questo l'ha detto lei stessa in qualche
              intervista). Dal momento che la serie si svolge
              principalmente in un posto (la casa dei Tendo) e
              quasi tutto è incentrato intorno a Ranma, e
              calcolando anche che si tratta di una commedia quindi
              se qualche personaggio raggiunge il "proprio scopo"
              la commedia finisce, allora i personaggi si evolvono
              molto lentamente.<br>
              Ad esempio, Ranma potrebbe tornare
              in Cina per curarsi (lo voleva fin dall'inizio della
              storia) ma se fosse stato curato subito sarebbero
              scomparse moltissime delle scene comiche, equivoci e
              così via. Allo stesso modo, se Ranma ed Akane si
              fossero sposati (o anche solo baciati), la serie sarebbe terminata alquanto
              velocemente. La stessa Takahashi ha detto che il
              "goal" finale della serie è il matrimonio di Ranma e
              Akane (alè oh ooh!! ^__^) o, almeno, una
              dichiarazione esplicita da parte dei due di amarsi;
              quindi, qualunque cosa possa accelerare il
              raggiungimento di questo obiettivo, non può
              accadere.</li>
              <li class="mt-2">Per alcune questioni non esiste una risposta
              precisa, si tratta quindi solo di teorie ed ipotesi.
              Questo anche per lasciare il plot della storia più
              interessante.</li>
            </ol>

