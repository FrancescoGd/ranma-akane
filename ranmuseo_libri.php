                <th nowrap="nowrap">Note</th>
                <td align="left" valign="top">
                  <p>Questo è praticamente l'unico libro di
                  illustrazioni propriamente detto, realizzato
                  dalla Takahashi su Ranma.</p>
                  <p>La confezione è come sempre ottima, con
                  sovracopertina plastificata e carta di alta
                  qualità.</p>
                  <p>Riguardo ai contenuti, oltre ovviamente a
                  tante bellissime illustrazioni (riutilizzate poi
                  in molti altri prodotti e che sono quelle che
                  solitamente si trovano in tutte le gallerie
                  online), sono presenti dei "paginoni" pieghevoli
                  con da un lato un poster che si unisce riunendo i
                  quattro paginoni e sulle altre facciate i profili
                  (intesi sia come disegno che come profilo
                  caratteriale) delle quattro ragazze principali
                  della storia: Akane, Ranma-chan, Ukyo e
                  Shampoo.</p>
                  <p>A parte questo ci sono anche diverse pagine di
                  editoriali, sui personaggi, un riassunto dei
                  punti salienti della storia fatto con le vignette
                  del manga, un grafico delle "relazioni" tra i
                  personaggi e un'intervista alla Takahashi.</p>
                  <p>Insomma, è abbastanza imperdibile :D</p>
                  <p><strong>Nota:</strong> quella che ho messo (e
                  che poi è quella che si vede in tutti i siti
                  quando si parla di Memorial Book) è
                  l'illustrazione della sovracopertina, la
                  copertina "vera" del volume ha l'illustrazione
                  usata nell'edizione americana, che potete vedere
                  qui sotto.</p>
